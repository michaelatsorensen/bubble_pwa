// ══════════════════════════════════════════════════════════
//  BUBBLE I18N — Internationalization system
//  Usage: t('key') or t('key', { name: 'value' })
//  Language stored in localStorage as bubble_lang
// ══════════════════════════════════════════════════════════

var _lang = localStorage.getItem('bubble_lang') || (navigator.language?.startsWith('da') ? 'da' : 'en');

function setLang(lang) {
  _lang = lang;
  localStorage.setItem('bubble_lang', lang);
  translateStaticUI();
}

function getLang() { return _lang; }

// Locale string for toLocaleDateString/toLocaleTimeString
function _locale() { return _lang === 'en' ? 'en-GB' : 'da-DK'; }

function t(key, vars) {
  var s = (_translations[_lang] && _translations[_lang][key]) || (_translations['da'] && _translations['da'][key]) || key;
  if (vars) {
    Object.keys(vars).forEach(function(k) {
      s = s.replace(new RegExp('\\{' + k + '\\}', 'g'), vars[k]);
    });
  }
  return s;
}

// ── Translate static HTML elements ──
function translateStaticUI() {
  // data-t="key" → textContent
  document.querySelectorAll('[data-t]').forEach(function(el) {
    el.textContent = t(el.dataset.t);
  });
  // data-t-placeholder="key" → placeholder
  document.querySelectorAll('[data-t-placeholder]').forEach(function(el) {
    el.placeholder = t(el.dataset.tPlaceholder);
  });
  // data-t-html="key" → innerHTML (for strings with nested elements)
  document.querySelectorAll('[data-t-html]').forEach(function(el) {
    el.innerHTML = t(el.dataset.tHtml);
  });
}

